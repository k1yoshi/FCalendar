    private void setStilo(final JButton boton)
    {
        boton.setBorderPainted(true);
        boton.setContentAreaFilled(false);
        boton.setCursor(Cursor.getPredefinedCursor( Cursor.HAND_CURSOR ));
        boton.setBackground(new Color(211, 211, 211));
        boton.setForeground(new Color(33, 33, 33));
        
        boton.setBorder(BorderFactory.createMatteBorder(
                                    1, 1, 1, 1, new Color(110,110,110)));
        boton.setOpaque(true);
        boton.setVisible(true);
        boton.setFocusPainted(false);
        
        boton.addMouseListener(new java.awt.event.MouseAdapter() {
            private Color button_released = new Color(0,0,0);
            private Color texto_released = new Color(255, 255, 255);
            
            private Color button_pressed = new Color(12,12,12);
            private Color texto_pressed = new Color(255,255,255);

         @Override
         public void mouseEntered(MouseEvent evt) {
         }
         @Override
            public void mouseClicked(MouseEvent e) {
                
            }

            @Override
            public void mousePressed(MouseEvent e) {
                if(boton.getClientProperty("%seleccionado%") == null)
                {
                    //capturamos valores iniciales
                    button_released = boton.getBackground();
                    texto_released = boton.getForeground();
                    //se colocan los nuevos colores
                    boton.setForeground( this.texto_pressed );
                    boton.setBackground( this.button_pressed );
                }
            }

            @Override
            public void mouseReleased(MouseEvent e) {
                if(boton.getClientProperty("%seleccionado%") == null)
                {
                    boton.setBackground( this.button_released );
                    boton.setForeground( this.texto_released );
                }
            }
            @Override
            public void mouseExited(MouseEvent e) {
                
            }
        });
    }
