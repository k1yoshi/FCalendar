    //<editor-fold defaultstate="collapsed" desc="esNumero">
    private boolean esNumero(String texto)
    {
        for(char caracter : texto.toCharArray())
        {
            if(!Character.isDigit(caracter))
                return false;
        }
        return true;
    }
