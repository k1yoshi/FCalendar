    private void comboAñosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboAñosItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED && cargaCompleta == true)
        {
            repintarButtons();
            año = Integer.parseInt(comboAños.getSelectedItem().toString());
            calendario.set(año, mes, 0);
            mostrarDias(año, mes, 0);
            comboAños.setVisible(false);
            lblTitulo.setVisible(true);
        }
    }//GEN-LAST:event_comboAñosItemStateChanged

