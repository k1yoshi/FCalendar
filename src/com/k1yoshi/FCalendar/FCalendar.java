    // <editor-fold defaultstate="collapsed" desc="crearArrayButtonsFin">
    private String[] crearArrayButtonsFin(String letra,int dia_semana)
    {
        int indice = 0;
        String[] array = new String[dia_semana];
        for(int i = 14; i > (14 - dia_semana); i --)
        {
            array[indice] = letra+(i+"");
            indice ++;
        }
        return array;
    }
