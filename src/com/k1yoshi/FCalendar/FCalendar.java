    // <editor-fold defaultstate="collapsed" desc="mostrarDias">
    private void mostrarDias(int a, int m, int d)
    {
        año = a;
        mes = m;
        dia = d;
        
        lblTitulo.setText(meses[mes]+"  "+ año);
        ocultarBotonesInicio(calendario.get(java.util.Calendar.DAY_OF_WEEK));
        ocultarBotonesFin(calendario.get(java.util.Calendar.DAY_OF_WEEK));
        
        final int inicial = 20 + ((8 - calendario.get(java.util.Calendar.DAY_OF_WEEK)) + 1);
        
        $("button").each(new Function<Yoshi>(){
            @Override
            public void internal(Yoshi d) {
                JButton button = (JButton)This;
                if(button.getText().length() > 0)
                {
                    if(button.isVisible())
                    {
                        if(esNumero(button.getText()))
                        {
                            int dia_act = Integer.parseInt(button.getText());
                            dia_act += (8 - calendario.get(java.util.Calendar.DAY_OF_WEEK));
                            button.setText(dia_act+"");
                            if(dia_act == dia_actual && mes == mes_actual && año == año_actual)
                            {
                                button.setBackground(fondo_dia_actual);
                                button.setForeground(texto_dia_actual);
                            }
                        }else{
                            switch (button.getText().substring(0, 1)) {
                                case "f":
                                    {
                                        int dia_act = Integer.parseInt(button.getText().substring(1,button.getText().length()));
                                        dia_act += inicial;
                                        button.setText(dia_act + "");
                                        if(dia_act == dia_actual && mes == mes_actual && año == año_actual)
                                        {
                                            button.setBackground(fondo_dia_actual);
                                            button.setForeground(texto_dia_actual);
                                        }
                                        break;
                                    }
                                case "p":
                                    {
                                        int dia_act = Integer.parseInt(button.getText().substring(1,button.getText().length()));
                                        dia_act -= (calendario.get(java.util.Calendar.DAY_OF_WEEK) -1);
                                        button.setText(dia_act + "");
                                        if(dia_act == dia_actual && mes == mes_actual && año == año_actual)
                                        {
                                            button.setBackground(fondo_dia_actual);
                                            button.setForeground(texto_dia_actual);
                                        }
                                        break;
                                    }
                            }
                        }
                    }
                }
            }
        });
    }
