    public String getDate(String formato)
    {
        if(dia ==0)
            formato = formato.replaceAll("d", dia_actual+"");
        else
            formato = formato.replaceAll("d", dia+"");
        formato = formato.replaceAll("m", (mes+1)+"");
        formato = formato.replaceAll("y", año+"");
        return formato;
    }
