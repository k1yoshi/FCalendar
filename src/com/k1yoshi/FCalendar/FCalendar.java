    private String[] crearArrayButtonsInicio(String letra,int dia_semana)
    {
        String[] array = new String[dia_semana - 1];
        for(int i = 1; i < dia_semana; i ++)
        {
            array[i-1] = letra+i;
        }
        return array;
    }
